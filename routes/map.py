def _classify(p: models.Project, today: date) -> dict:
    """Определяет тип маркера и метаданные для отображения на карте."""
    opened = bool(p.opening_date and p.opening_date <= today)
    days = (p.end_date - today).days if p.end_date else None

    if opened:
        marker = "opened"         # золотая звезда
    elif p.status == "Завершён":
        marker = "done"           # серый
    elif days is not None and days < 0:
        marker = "overdue"        # красный
    elif days is not None and days <= 14:
        marker = "urgent"         # оранжевый
    elif p.status == "Планирование":
        marker = "planning"       # синий
    else:
        marker = "active"         # зелёный

    return {
        "lat": None, "lng": None,
        "name": p.name or f"ТК {p.tk_number}",
        "tk": p.tk_number or "",
        "city": (p.city or "").strip(),
        "status": p.status,
        "type": p.project_type or "",
        "manager": p.manager.name if p.manager else "—",
        "deadline": p.end_date.strftime("%d.%m.%Y") if p.end_date else "—",
        "opening_date": p.opening_date.strftime("%d.%m.%Y") if p.opening_date else None,
        "days": days,
        "marker": marker,
        "url": f"/projects/{p.id}",
    }


@router.get("/map", response_class=HTMLResponse)
async def map_page(
    request: Request,
    db: Session = Depends(get_db),
    user: dict = Depends(require_login),
):
    today = date.today()
    projects = (
        db.query(models.Project)
        .options(joinedload(models.Project.manager))
        .all()
    )

    features: list[dict] = []
    skipped = 0
    for p in projects:
        info = _classify(p, today)
        city = info["city"]
        coords = CITY_COORDS.get(city)
        if not coords:
            skipped += 1
            continue
        info["lat"] = coords[0]
        info["lng"] = coords[1]
        features.append(info)

    stats = {
        "total": len(features),
        "active": sum(1 for f in features if f["marker"] == "active"),
        "urgent": sum(1 for f in features if f["marker"] == "urgent"),
        "overdue": sum(1 for f in features if f["marker"] == "overdue"),
        "opened": sum(1 for f in features if f["marker"] == "opened"),
        "done": sum(1 for f in features if f["marker"] == "done"),
        "planning": sum(1 for f in features if f["marker"] == "planning"),
        "skipped": skipped,
    }

    return templates.TemplateResponse("map.html", {
        "request": request,
        "user": user,
        "features": features,
        "stats": stats,
        "today": today,
    })
